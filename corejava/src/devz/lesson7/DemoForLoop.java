
package devz.lesson7;


public class DemoForLoop {
    
    
    public static void main(String[] args) {
    
        // 1 - > 2
        System.out.println("Start");
        
        
        for(int i=0; i < 3 ; ++i  ){
            System.out.println("I love java " + i);
        }
        
        System.out.println("End");
        
    }
    
    
    
    
}
